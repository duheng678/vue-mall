<template>
  <div class="goods-info" v-if="Object.keys(detailInfo).length !== 0">
    <div class="info-desc">
      <div class="start"></div>
      <div class="desc">{{ detailInfo.desc }}</div>
      <div class="end"></div>
    </div>
    <div class="info-key">{{ detailInfo.detailImage[0].key }}</div>
    <div class="info-img">
      <img
        v-for="(item, index) in detailInfo.detailImage[0].list"
        :key="index"
        :src="item"
        @load="imageLoad"
        alt=""
      />
    </div>
  </div>
</template>

<script>
export default {
  name: "DetailGoodsInfo",
  props: {
    detailInfo: {
      type: Object,
      defautl: {}
    }
  },
  data() {
    return {
      count: 0,
      imagesLength: 0
    };
  },
  methods: {
    //判断图片是否加载完毕，所有图片加载完毕后再想Detail组件发送
    imageLoad() {
      if (++this.count === this.imagesLength) {
        this.$emit("imageLoad");
      }
    }
  },
  watch: {
    //监控请求到了多少张图片
    detailInfo() {
      this.imagesLength = this.detailInfo.detailImage[0].list.length;
    }
  }
};
</script>

